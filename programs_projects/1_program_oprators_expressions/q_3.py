#3. WAP to input the cost price and profit to be obtained of an item. Calculate and display the selling price
cost_price = float(input("Enter the cost price of the item: "))
profit = float(input("Enter the profit to be obtained: "))
selling_price = cost_price + profit
print(f"The selling price of the item is {selling_price}.")
# Output: Enter the cost price of the item: 100
# Enter the profit to be obtained: 50
# The selling price of the item is 150.0.

